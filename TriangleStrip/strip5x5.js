
function setup() {
    createCanvas(1000, 580);
    background(204);
}

function draw() {
    background(204);

    var indexes = [
         0,  5,  1,  6,  2,  7,  3,  8,  4,  9,
         9, 14,  8, 13,  7, 12,  6, 11,  5, 10,
        10, 15, 11, 16, 12, 17, 13, 18, 14, 19,
        19, 24, 18, 23, 17, 22, 16, 21, 15, 20];
    var points = [[0, 0], [1320, 0], [1520, 0], [1720, 0], [1920, 0], [0, 270], [1320, 270], [1520, 270], [1720, 270], [1920, 270], [0, 540], [1320, 540], [1520, 540], [1720, 540], [1920, 540], [0, 810], [1320, 810], [1520, 810], [1720, 810], [1920, 810], [0, 1080], [1320, 1080], [1520, 1080], [1720, 1080], [1920, 1080]];

    beginShape(TRIANGLE_STRIP);
    for (var i = 0; i < indexes.length; i++) {
        var x = points[indexes[i]][0] / 2 + 20;
        var y = points[indexes[i]][1] / 2 + 20;
        vertex(x, y);
    }
    endShape();
}